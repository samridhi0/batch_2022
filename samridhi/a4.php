<h1 style="text-align:center;font-size:300%;font-style:italic;">INCREMENT AND DECREMENT OPERATORS</h1>
<p style="font-size:250%;font-style:cursive;">The increment operators are used to
increment a variable's value</p>
<h3 style="font-style:cursive;font-size:150%;">*PRE-INCREMENT OPERATOR</h3>
<?php
$x = 100;  
echo ++$x;
?>  
<h3 style="font-style:cursive;font-size:150%;">*POST-INCREMENT OPERATOR</h3>
<?php
$x = 150;  
echo $x++;
?>  
<p style="font-size:200%;font-style:cursive;">The decrement operators are used to decrement a variable's value.</p>
<h3 style="font-style:cursive;font-size:150%;">*PRE-DECREMENT OPERATOR</h3>
<?php
$x = 150;  
echo --$x;
?>  
<h3 style="font-style:cursive;font-size:150%;">*POST-DECREMENT OPERATOR</h3>
<?php
$x = 100;  
echo $x--;
?>
