<h1 style="text-align:center;font-size:300%;font-style:italic;">ASSIGNMENT OPERATORS</h1>
<p style="font-size:150%;font-style:cursive;">The assignment operators are used with numeric
values to write a value to a variable.The basic assignment operator in PHP is "=". It means that 
the left operand gets set to the value of the assignment expression on the right.</p>
<h4 style="font-size:200%;font-style:cursive;">*assignment</h4>
<?php
$x = 10;  
echo $x;
?> 
 
<h4 style="font-style:cursive;font-size:200%;">*addition</h4>
<?php
$x = 120;  
$x += 100;
echo $x;
?> 
 
<h4 style="font-style:cursive;font-size:200%;">*subtraction</h4>
<?php
$x = 150;
$x -= 20;
echo $x;
?>  

<h4 style="font-style:cursive;font-size:200%;">*Multiplication</h4>
<?php
$x = 5;
$x *= 6;

echo $x;
?>  

<h4 style="font-style:cursive;font-size:200%;">Division</h4>
<?php
$x = 150;
$x /= 20;               
echo $x;
?>  

<h4 style="font-style:cursive;font-size:200%;">*Modulus</h4>
<?php
$x = 150;
$x %= 20;

echo $x;
?>  
